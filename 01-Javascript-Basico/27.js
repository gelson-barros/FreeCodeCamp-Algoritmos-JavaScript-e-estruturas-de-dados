/*
Concatenando Strings com o Operador Plus Equals

Também podemos usar o += operador para concatenar uma string no final de uma variável de string existente. Isso pode ser muito útil para quebrar uma corda longa em várias linhas.

Nota: Cuidado com os espaços. A concatenação não adiciona espaços entre strings concatenadas, portanto, você mesmo precisará adicioná-los.

Exemplo:

var ourStr = "I come first. ";
ourStr += "I come second.";
ourStr agora tem um valor de string I come first. I come second..

Construa myStr sobre várias linhas concatenando essas duas strings: This is the first sentence. e This is the second sentence.usando o += operador. Use o += operador semelhante ao mostrado no exemplo. Comece atribuindo a primeira string a e myStr, em seguida, adicione a segunda string.
*/

// Only change code below this line

var myStr = "This is the first sentence. ";
myStr += "This is the second sentence";
/*
myStr deve ter um valor da string This is the first sentence. This is the second sentence.

Você deve usar o += operador para construir myStr.
*/